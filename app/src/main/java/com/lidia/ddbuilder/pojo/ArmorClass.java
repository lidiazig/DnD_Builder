package com.lidia.ddbuilder.pojo;

public class ArmorClass {
    private int ac;
    private int touch;
    private int flatfooted;
    private int armor;
    private int shield;
    private int dex;
    private int size;
    private int naturalArmor;
    private int deflection;
    private int misc;

    public int getAc() {
        return ac;
    }

    public void setAc(int ac) {
        this.ac = ac;
    }

    public int getTouch() {
        return touch;
    }

    public void setTouch(int touch) {
        this.touch = touch;
    }

    public int getFlatfooted() {
        return flatfooted;
    }

    public void setFlatfooted(int flatfooted) {
        this.flatfooted = flatfooted;
    }

    public int getArmor() {
        return armor;
    }

    public void setArmor(int armor) {
        this.armor = armor;
    }

    public int getShield() {
        return shield;
    }

    public void setShield(int shield) {
        this.shield = shield;
    }

    public int getDex() {
        return dex;
    }

    public void setDex(int dex) {
        this.dex = dex;
    }

    public int getSize() {
        return size;
    }

    public void setSize(int size) {
        this.size = size;
    }

    public int getNaturalArmor() {
        return naturalArmor;
    }

    public void setNaturalArmor(int naturalArmor) {
        this.naturalArmor = naturalArmor;
    }

    public int getDeflection() {
        return deflection;
    }

    public void setDeflection(int deflection) {
        this.deflection = deflection;
    }

    public int getMisc() {
        return misc;
    }

    public void setMisc(int misc) {
        this.misc = misc;
    }
}
